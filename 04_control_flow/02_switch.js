const month = "march";

switch (month) {
    case "january":
        console.log("01");
        break;
    case "febuary":
        console.log("02");
        break;
    case "march":
        console.log("03");
        break;
    case "April":
        console.log("04");
        break;
    
    default:
        console.log("Default Case Matched");
        break;
}